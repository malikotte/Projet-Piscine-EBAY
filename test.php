\normalsize
Automotive radar are used in vehicles to add comfort and safety for drivers. This provides the driver with more information about his environment, moreover the vehicle can be brought to maneuver without the intervention of the driver.
The purpose of the radar is to estimate the speed and direction of travel. Indeed, the radar will emit an electromagnetic radio signal which will be reflected by a target, on its return the signal can be examined by what is called "signal processing". Most radars are characterized by the emission of continuous waves, which can be modulated or not, and using the Doppler effect. In the case of an unmodulated wave, only one parameter can be obtained between the speed and the direction of movement of the target.

Depending on the type of speed camera, different information can be retrieved. There are short-range radars which are responsible for detecting obstacles close to the vehicle and long-range radars which ensure compliance with the safety distance. With advances in technology, these radars are more and more used in current cars since the arrival of connected / autonomous cars.
